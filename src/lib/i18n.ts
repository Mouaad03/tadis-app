export function getLang(): Lang {
  if (typeof window === 'undefined') return 'en'
  const saved = localStorage.getItem('tradis_lang') as Lang
  if (saved && ['en','fr','es'].includes(saved)) return saved
  const browser = navigator.language.split('-')[0] as Lang
  if (['en','fr','es'].includes(browser)) return browser
  return 'en'
}

export function setLang(lang: Lang) {
  localStorage.setItem('tradis_lang', lang)
  window.location.reload()
}

export function t(key: keyof typeof translations.en, lang: Lang): string {
  const safeLang = (['en','fr','es'].includes(lang) ? lang : 'en') as Lang
  return (translations[safeLang] as any)?.[key] || translations.en[key] || key
}
